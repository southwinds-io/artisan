package conf

// MergeMaps merge two or more maps
// the latter map overrides the former if duplicate keys exist across the two maps
func MergeMaps(ms ...map[string]string) map[string]string {
	res := map[string]string{}
	for _, m := range ms {
		for k, v := range m {
			res[k] = v
		}
	}
	return res
}
